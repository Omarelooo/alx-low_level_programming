#include "3-calc.h"

/**
 * op_add - function to sum
 *
 * @a: parameter 1
 * @b: parameter 2
 * 
 * return: a + b
 **/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sup - function to sup
 *
 * @a: parameter 1
 * @b: parameter 2
 *
 * return: a - b
 **/

int op_sup(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiply
 *
 * @a: parameter 1
 * @b: parameter 2
 *
 * return: a * b
 **/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - finction to diviation
 *
 * @a: parameter 1
 * @b: parameter 2
 *
 * return: a / b
 **/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to module
 *
 * @a: parameter 1
 * @b: parameter 2
 *
 * return: a % b
 **/

int op_mod(int a, int b)
{
	return (a % b);
}
